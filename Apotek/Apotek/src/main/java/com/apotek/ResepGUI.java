    private void populateTable() {
       DefaultTableModel model = (DefaultTableModel) tblApoteker.getModel();
       model.setRowCount(0); // Mengosongkan tabel
       model.setColumnIdentifiers(new Object[]{"ID Resep", "Tanggal Resep", "ID Pasien", "ID Apoteker"});

       try {
            Database db = new Database();
            db.openConnection();
            db.statement = db.connection.createStatement();

            String query = "SELECT * FROM resep";
            ResultSet resultSet = db.statement.executeQuery(query);

            while (resultSet.next()) {
                int id = resultSet.getInt("id_resep");
                String tgl = resultSet.getString("tanggal_resep");
                String pasien = resultSet.getString("pasien_id_pasien");
                String apoteker = resultSet.getString("apoteker_id_apoteker");

                model.addRow(new Object[]{id, tgl, pasien, apoteker});
            }

            resultSet.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            // Handle exceptions
        }    
    }
    
    public class DateValidator {
        public static boolean isValidDateFormat(String date, String format) {
            SimpleDateFormat sdf = new SimpleDateFormat(format);
            sdf.setLenient(false); // Non-lenient parsing

            try {
                Date parsedDate = sdf.parse(date);
                return date.equals(sdf.format(parsedDate)); // Ensure the date is exactly the same as input
            } catch (ParseException e) {
                return false;
            }
        }
    }
    
    private boolean isIdDuplicate(String id) {
        try {
            Database db = new Database();
            db.openConnection();
            db.statement = db.connection.createStatement();

            String query = "SELECT * FROM resep WHERE id_resep = " + id;
            ResultSet resultSet = db.statement.executeQuery(query);

            return resultSet.next(); // Mengembalikan true jika ID sudah ada, false jika belum ada

        } catch (SQLException ex) {
            ex.printStackTrace();
            // Handle exceptions
        }
        return false;
    }
    
    private boolean isInteger(String s) {
        try {
            Integer.parseInt(s);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
