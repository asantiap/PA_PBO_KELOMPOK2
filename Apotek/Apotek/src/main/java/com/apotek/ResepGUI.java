    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String id_resep = txtID.getText();
        
        String pasien_id_pasien = txtIDPAS.getText();
        String apoteker_id_apoteker = txtIDAPT.getText();
        String tanggal_resep = txtTGL.getText();
        
        if (isIdDuplicate(id_resep)) {
            JOptionPane.showMessageDialog(this, "ID sudah ada dalam database.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (id_resep.isEmpty() || pasien_id_pasien.isEmpty() || apoteker_id_apoteker.isEmpty() || tanggal_resep.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!ObatGUI.DateValidator.isValidDateFormat(tanggal_resep, "dd-MM-yy")) {
            JOptionPane.showMessageDialog(this, "Format tanggal tidak valid. Gunakan format dd-MM-yy", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!isInteger(id_resep)) {
            JOptionPane.showMessageDialog(this, "ID harus berupa angka.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            int id = Integer.parseInt(id_resep);
            // Buat objek Resep dan isi dengan data yang diambil dari GUI
            Resep resep = new Resep();
            resep.id_resep = id;
            resep.pasien_id_pasien = pasien_id_pasien;
            resep.apoteker_id_apoteker = apoteker_id_apoteker;
            resep.tanggal_resep = tanggal_resep;
            
            // Tambahkan data ke database
            if (resep.create()) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                // Bersihkan input setelah berhasil menambahkan data
                clearFields();
                // Perbarui tabel setelah menambahkan data baru
                populateTable();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

